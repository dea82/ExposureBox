void Buzz_init(void)
{
    gpio_conf(BUZZ_CFG, OUTPUT, 0);
}

void Buzz_10ms(void)
{
    static tU16 time = 0;
    time++;
    if(time > 20)
    {
        //gpio_set(BUZZ_CFG);
        time = 0;
    }
    else if (time > 1) {
        gpio_clr(BUZZ_CFG);
    }
}
